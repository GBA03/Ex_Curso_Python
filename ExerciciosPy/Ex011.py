l = float(input('Informe a largura em metros da parede:'))
al = float(input('Informe a altura em metros da parede:'))
area = l*al
tinta = area / 2

print(f'A largura dessa parede é {l}m e a altura é {al}m, assim a área dessa parede é de {area} metros²')
print(f'A quantidade de tinta necessaria para pintar essa parede é de: {tinta} Litros')
